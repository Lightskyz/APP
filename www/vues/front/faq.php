		<h1>Foire Aux Questions (F.A.Q.)</h1>

		<h3>Peut-on acheter sans s’inscrire ?</h3><br />
		<p>Non, pour des raisons de sécurité, il faut être inscrit pour acheter, échanger ou vendre un produit. L’inscription est gratuite mais il faut lier une carte de crédit à votre compte pour ne permettre à chaque utilisateur de n’avoir qu’un compte, ainsi, vous êtes sûr d’avoir affaire à la bonne personne lorsque vous êtes en contact avec un autre utilisateur!</p>

<hr>

<h3>Comment se passe la transaction ?</h3><br />
		<p> Pour effectuer une transaction, rien de plus simple! Une fois votre produit choisi grâce à la barre de recherche, il vous suffit de cliquer sur Acheter, ou Échanger et de confirmer votre achat/échange. Une fois l’achat/échange confirmé, les coordonnées du vendeur vous sont transmises, et les vôtres lui sont également transmises. Il ne vous reste plus qu’à vous arranger pour finaliser la vente ou l’échange: vous retrouvez le vendeur et lui achetez/échangez son produit</p>

<hr>

<h3>Peut-on payer par carte bleue ?</h3><br />
		<p>Non, le système de Pear2Pear est basé sur la confiance et le paiement s’effectue directement entre les utilisateurs. C’est à vous de décider avec votre vendeur/acheteur comment le paiement s’effectuera.</p>

<hr>

<h3>J’ai oublié mon mot de passe ?</h3><br />
		<p>Si vous avez oublié votre mot de passe, il vous suffit de vous rendre sur la page de connexion et de cliquer sur “Mot de passe oublié”. Il ne vous reste plus qu’à suivre les instructions. Après vérification de votre identité, votre nouveau mot de passe vous sera envoyé par mail immédiatement.</p>

<hr>

<h3>Comment on rentre en contact avec le vendeur ou l’acheteur ?</h3><br />
		<p>Dès qu’une transaction est confirmée, les coordonnées du vendeur/acheteur vous sont transmises par e-mail pour vous permettre de rentrer en contact simplement et immédiatement.</p>

<hr>

<h3>Comment être sûr que les produits sont de qualités ? Avez-vous une garantie ?</h3><br />
		<p>Pear2Pear est un site basé sur la confiance. Tous nos utilisateurs sont invités à se noter entre eux pour garantir le sérieux de chacun ainsi que la qualité des produits. 95% de nos utilisateurs ont une moyenne supérieure à 4 sur 5, pour connaitre le sérieux d’un vendeur et par conséquent la qualité de ses produits, il vous suffit de vous rendre sur son profil!</p>

<hr>

<h3>Est ce que tout le monde peut poster une annonce ?</h3><br />
<p>Oui, tous les utilisateurs inscrits et disposant d’un produit à vendre peuvent poster une annonce.</p>

<hr>

<h3>En combien de temps notre annonce est-elle publiée ?</h3><br />
<p>Immédiatement! Votre annonce n’a pas besoin d’être validée pour être publiée pour ne pas perdre de temps. Mais toutes les annonces sont examinées par nos administrateurs et sont susceptibles d’être supprimées si elles ne respectent pas les règles de Pear2Pear.</p>

<hr>

<h3>Qui sommes nous ?</h3><br />
<p> Nous sommes une association à but non-lucratif ayant pour but de faire perdurer l’agriculture à domicile et le partage entre agriculteurs. Anciennement nommée de “De potager à particulier”, notre association fait aujourd’hui peau neuve et s’adapte au monde numérique avec un nouveau site Internet.</p>

<hr>

<h3>Comment gagnez vous de l’argent ?</h3><br />
<p>En tant d'association, nous ne gagnons pas d’argent grâce au site Internet, pas de comission, pas de pub, tout pour les fruits et légumes! L’hébergement est les frais de maintenance du site Internet sont assurés par les cotisations des membres de l’association “De potager à particulier”.
</p>

<hr>

<h3>Mon compte a été banni, pourquoi ?</h3><br />
<p>Si vous ne parvenez plus à vous connecter à votre compte, c’est que vous avez probablement enfreint une règle. Pour éviter cela, nous vous invitons à consulter la page <a href="cgu.php"> CGU </a> </p>

